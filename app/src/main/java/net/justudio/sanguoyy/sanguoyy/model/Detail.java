package net.justudio.sanguoyy.sanguoyy.model;

/**
 * 三国演义各回目具体内容的实例
 */
public class Detail {


    private String content;
    private String link;
    private int state;


    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}
